package for2;

/**
 *
 * @author Hesham
 */
public class For2 {

    public static void main(String[] args) {
        for(int i = 0; i <= 8; i++)
        {
            for(int x = 0; x <= i; x++)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
    
}
